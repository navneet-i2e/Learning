//to convert negative to positive
console.log(Math.abs(-10));
//to get the maximum value
console.log(Math.max(10, 120, 90, 250));
//to get the minimum value
console.log(Math.min(10, 120, 5));
//to get the power of value
console.log(Math.pow(2, 3));
//to get the square root of value
console.log(Math.sqrt(16));
//to get the round off value
console.log(Math.round(10.4));
//to get the floor value
console.log(Math.floor(10.4));
console.log(Math.floor(-3.5));
//to get the ceil value
console.log(Math.ceil(10.4));
console.log(Math.ceil(-3.5));
//to get the cube root of value
console.log(Math.cbrt(27));
//to get the exponential value
console.log(Math.exp(2));
//to get the log value
console.log(Math.log10(10000));
//to get the natural log value
console.log(Math.log2(2));

console.log(Math.random()); // returns a random real number between 0 and 1 (excluded)
