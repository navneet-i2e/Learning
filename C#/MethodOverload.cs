class MethodOverloading{
    public static int add(int a,int b)
    {
        return a + b;
    }
    public static double add(double a,double b)
    {
        return a + b;
    }
    public static string add(string a,string b)
    {
        return a + b;
    }
    public static float add(float a,float b)
    {return a + b;}
}